
public class TypeCasting {
    public static void main(String args[]){
        float f=25.26f;
        int i=(int)f;  //type casting
        System.out.println(i);

        float fl=34.34f;
        System.out.println((int)fl);  //type cosing

        char ch='a';
        int a=ch;
        System.out.println(a); //ascii value of a will print on the screen

    }
}

